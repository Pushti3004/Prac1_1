fun main(){
    var x:Int=10
    var y:Double=x.toDouble()
    var p:String="10"
    var q:Int=p.toInt()
    var r:Double=q.toDouble()
    println("Integer to Double:")
    println(x)
    println(y)
    println("String to Integer:")
    println(q)
    println(p)
    println("String to Double:")
    println(q)
    println(r)
}